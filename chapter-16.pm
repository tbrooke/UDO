Section 16.1 Authority and Duties

The Board of Adjustment shall have the following duties and responsibilities:

To hear and decide appeals from any ruling, decision, determination, or interpretation made by the Zoning Administrator pursuant to or regarding these regulations.

To hear and decide petitions for variances from the requirements of the regulations of this Ordinance.

To make an interpretation of any portion of this Ordinance.

To change the use of, or expand certain nonconformities in accordance with Section 12.3.

To serve as the Watershed Review Board and rule on all petitions in accordance with the procedures set forth in Section 3.16.


Section 16.2 Membership

16.2.1 Regular Members
Board shall consist of seven (7) members; five (5) members of the Board shall be appointed by the Town Council and shall be residents of the Town of China Grove, and two (2) members shall be appointed by the Board of County Commissioners of Rowan County, all for overlapping terms of three (3) years.  Initial appointment of the Town members shall be as follows:  two (2) members for a term of three (3) years, two (2) members for a term of two (2) years, and one (1) member for a term of one (1) year.  Initial appointment of the County members shall be as follows: One (1) member for a term of three (3) years, and one (1) member for a term of one (1) year.  Any vacancy in the membership shall be filled for the unexpired term in the same manner as the initial appointment.  The two (2) members appointed by the Board of County Commissioners as representatives of the extraterritorial area outside the Town of China Grove shall be residents of such area and citizens of Rowan County.  Such members shall have equal rights, privileges, and duties with other members of the Board in all matters.  The concurring vote of six (6) members of the Board shall be necessary to reverse any order, requirement, decision or determination of any administrative official charged with enforcement of this Ordinance or to decide in favor of the applicant any matter upon which it is required to pass under the Zoning Ordinance or to affect any variation of such Ordinance.

16.2.2 Alternate Members
In addition to the regular members, the Town Council may appoint one (1) alternate member, who shall be a resident of the Town of China Grove and the Board of Commissioners of Rowan County may appoint one (1) alternate member, who shall be a resident of the extraterritorial area, each for three (3) year terms.  The Chairman of the Board of Adjustment, or in his absence the acting chairman, may appoint the alternates to sit for any regular members in case of the absence or disqualification of any regular members.  In such case the alternate members shall have the same powers and duties of the regular members they are replacing during such time.  In no case, however, shall more than seven (7) regular members or combination of regular members and the alternate members be empowered to vote on any matter that comes before the Board.  




Section 16.3 Meetings, Hearings, and Procedures

16.3.1 General Provisions
The Board of Adjustment shall hold a public hearing on an application no later than 45 days after a complete application has been filed with the Zoning Administrator.  The Board of Adjustment shall decide on the matter which was presented at the public hearing within 45 days of the close of the public hearing.

All meetings and hearings shall be open to the public and shall be conducted in accordance with the procedure set forth in these regulations and rules of procedure adopted by the Board of Adjustment.  Such rules of procedures may be amended by the Board of Adjustment membership.

Any rules of procedure adopted by the Board of Adjustment shall be kept on file at the offices of the Zoning Administrator and shall be made available to the public at any meeting or hearing of the Board of Adjustment.

The Board of Adjustment shall elect a chairman and a vice‑chairman from its members who shall serve for one (1) year or until re‑elected or until their successors are elected.  The Board shall appoint a secretary, who may be municipal officer, an employee of the Town, or a member of the Board of Adjustment.  The Board shall adopt rules and by‑laws in accordance with the provisions of this Ordinance and of NCGS Article 19, Chapter 160A.  Meetings of the Board shall be held at the call of the chairman and at such other times as the Board may determine.  The chairman, or in his absence the vice‑chairman, may administer oaths and compel the attendance of witnesses by subpoena.  All meetings of the Board shall be open to the public.  Conflicts of interest shall be disclosed as per NCGS 160A-388.

16.3.2 Notice of Public Hearings
The Town of China Grove shall give notice of all public hearings for Board of Adjustment proceedings.  Said notice shall become a part of the record of the proceedings of the Board of Adjustment.  Notice shall be given in the following manner:

Notice shall be sent by the Town by first class mail to the applicant and the owners of all contiguous properties at least 10 days prior to the public hearing.  Additionally, for Watershed Variances the Town shall also send notice of the hearing plus a description of the variance request by first class mail to the Clerk of all municipal and county governments having jurisdiction within the same watershed and to any entity using the water supply from that watershed for consumption purposes at least ten 10 days prior to the public hearing.  The notice shall indicate the nature of the public hearing and the date, time and place at which it is to occur.
	
A notice shall be published in a newspaper having general circulation in the Town once provided that the notice is published not less than seven (7) days nor more than 15 days prior to the date established for the public hearing.

16.3.3 Decisions
The concurrent vote of four-fifths (4/5) of the voting members of the Board of Adjustment shall be necessary to make an interpretation of the Zoning Ordinance, reverse any order, requirement, decision or determination of the Zoning Administrator, approve a variance or change or replacement of a nonconformity.  A vacant or disqualified seat shall not be used in determining the vote.  This provision does not include absences of members.  In all matters coming before the Board of Adjustment, the applicant shall have the burden of providing clear, competent and material evidence in support of the application.
All decisions, by the Board of Adjustment shall be in writing and filed with the Zoning Administrator.  A written copy of decisions on variances shall be sent by first class mail to the applicant within 10 days of the date of decision of the Board of Adjustment.

Upon the denial of an original application, or upon the denial of an application from which a rehearing has been conducted, a similar application may not be filed for a period of one (1) year after the date of denial of the original application.


Section 16.4 Staff

The Zoning Administrator or his/her designee shall serve as staff to the Board of Adjustment and shall provide technical assistance to the Board of Adjustment as requested.


Section 16.5 Applications

Before a petition for an appeal, variance, or change or replacement of a nonconformity, shall be heard by the Board of Adjustment, an application shall be submitted to the Zoning Administrator along with a fee in accordance with fee schedule established by the Town Council.  Said fee shall be waived for any petition initiated by the Zoning Administrator or other officials of China Grove who initiate a request on behalf of China Grove.  For variance requests, the application shall be accompanied by a map clearly identifying the subject property, all contiguous pieces of properties.  In addition, a list of names and addresses of the owners of said properties, from the most recent official tax records, shall be provided by the applicant.


Section 16.6 Appeals

16.6.1 Appeal Initiation
An appeal may be made by any person who has received a ruling, decision, determination, or interpretation from the Zoning Administrator. An appeal to the Board of Adjustment shall be made within 30 days after the receipt of the decision, order, determination, or interpretation made by the Zoning Administrator.  The Zoning Administrator shall transmit to the Board all papers constituting the record upon which the action appealed from was taken. 

16.6.2 Stay of Proceedings
An appeal stays all legal proceedings in furtherance of the action appealed from, unless the Zoning Administrator certifies to the Board of Adjustment after the notice of appeal has been filed with him, that by reason of facts stated in the certificate a stay would, in his opinion, cause imminent peril to life and property.  In such case, proceedings shall not be stayed otherwise than by a restraining order which may be granted by the Board of Adjustment or by a court of record on application, on notice to the Zoning Administrator, on due cause shown.

16.6.3 Public Hearing
The Board of Adjustment may, after having held a quasi-judicial public hearing on the matter, reverse or affirm, wholly or partly, or may modify the order, requirement, decision or determination appealed.  On all appeals, applications and other matters brought before the Board of Adjustment, said Board shall inform in writing all the parties involved of its decisions and the reasons therefore.

Section 16.7 Variances

16.7.1 Intent
When practical difficulties, special conditions, or unnecessary hardships would result from carrying out the strict letter of this Ordinance, the Board of Adjustment shall have the power to vary or modify any of the regulations or provisions of this Ordinance relating to the construction or alteration of buildings or structures or the use of land so that the spirit of the Ordinance shall be observed, public safety and welfare secured, and substantial justice done.

16.7.2 Public Hearing
No variance shall be granted until a quasi-judicial public hearing is held by the Board of Adjustment.  Due notice of the public hearing shall by given to all affected parties pursuant to NCGS 153A-345.

Such variance may be granted in such individual case of unnecessary hardship where the Board of Adjustment makes the following findings:

There are practical difficulties or unnecessary hardships in the way of carrying out the strict letter of the Ordinance:
If the provisions of the Ordinance are strictly adhered to, the property owner can secure no reasonable return from or make no reasonable use of, his property.
The hardship results from the application of the Ordinance.
The hardship is suffered by the applicant's property.
The hardship is not a result of the applicant's own actions.
The hardship is peculiar to the applicant's property.

The variance is in harmony with the general purpose and intent of the Ordinance and preserves its spirit.

In granting the variance the public safety and welfare have been assured and substantial justice has been done.

Conditions may be imposed by the Board of Adjustment to mitigate the effects created by granting the variance.

16.7.3 Watershed Variances
The Board of Adjustment may authorize variances from the specific requirements of the Watershed Overlay Districts in the same manner and subject to the same procedures and requirements of this Ordinance for authorizing other variances, provided that:

A notice be mailed by first class mail to all other local governments having watershed regulation jurisdiction within the particular watershed regulation jurisdiction within the particular watershed where the variance is requested and to each entity using that water supply for consumption; 

If the variance request is for a major variance as defined herein the following procedure shall apply.  If the Board of Adjustment decides in favor of granting the major variance, the Board shall then prepare a preliminary record of the hearing and submit it to the North Carolina Environmental Management Commission (EMC) for review and action.  If the Board does not decide in favor of granting the major variance such unfavorable action shall constitute denial;

In the event of a favorable action by the Board on a major variance, the Board, shall cause the record of their hearing to be promptly submitted to the EMC.  The record of the hearing shall include but not be limited to:

The variance application;
The hearing notices;
The evidence presented;
Motions, offers of proof, objections to evidence and rulings on them;
Findings and exception; and
The action of the Board including any conditions proposed.

If the EMC approves the major variance or approves with conditions or stipulations added, the EMC shall prepare a decision which authorizes the Board of Adjustment to issue a final decision which would include any conditions or stipulations added by the EMC.  If the EMC denies the major variance, then the EMC shall prepare a decision and transmit it to the Board of Adjustment.  The Board shall then prepare a final decision denying the major variance.

The Board of Adjustment, may only grant a Variance, having first held a public hearing on the matter and having made the following determinations:

There are practical difficulties or unnecessary hardships in the way of carrying out the strict letter of the Ordinance; and

That the Variance is in harmony with the general purpose and intent of this Ordinance and preserves its spirit; and

That in the granting of the Variance, the public safety and welfare have been assured and substantial justice has been done; and

That the reasons set forth in the application justify the granting of a Variance, and that the Variance is a minimum one that will make possible the reasonable use of land or structures.

The Board of Adjustment, in approving all Variances, may prescribe appropriate conditions and safeguards in conformity with this Ordinance.  Violation of such conditions and safeguards, when made a part of the terms under which a Variance is approved, shall be deemed a violation of this Ordinance and shall be punishable as prescribed in Chapter 14 of this Ordinance.

Unless otherwise authorized by the Board of Adjustment and included in its decision to approve a Variance, any order of the Board of Adjustment in approving a variance shall expire, if a building permit, or certificate of occupancy (for a use for which a building permit is not required), has not been obtained within one (1) year from the date of its decision.

16.7.4 Floodplain Variance Procedures 
The Board of Adjustment as established by the Town of China Grove shall hear and decide requests for floodplain variances from the requirements of this section.

Any person aggrieved by the decision of the appeal board may appeal such decision to the Court, as provided in Chapter 7A of the North Carolina General Statutes.



Floodplain variances may be issued for:

The repair or rehabilitation of historic structures upon the determination that the proposed repair or rehabilitation will not preclude the structure's continued designation as a historic structure and that the variance is the minimum necessary to preserve the historic character and design of the structure; or

Functionally dependent facilities if determined to meet the definition as stated in Appendix A of this Ordinance, provided provisions of 16.7.4.I have been satisfied, and such facilities are protected by methods that minimize flood damages during the base flood and create no additional threats to public safety; or 

Any other type of development, provided it meets the requirements of this subsection.

In passing upon floodplain variances, the appeal board shall consider all technical evaluations, all relevant factors, all standards specified in this section, and:

The danger that materials may be swept onto other lands to the injury of others;

The danger to life and property due to flooding or erosion damage;

The susceptibility of the proposed facility and its contents to flood damage and the effect of such damage on the individual owner;

The importance of the services provided by the proposed facility to the community;

The necessity to the facility of a waterfront location as defined in Appendix A of this ordinance as a functionally dependent facility, where applicable;

The availability of alternative locations, not subject to flooding or erosion damage, for the proposed use;

The compatibility of the proposed use with existing and anticipated development;

The relationship of the proposed use to the comprehensive plan and floodplain management program for that area;

The safety of access to the property in times of flood for ordinary and emergency vehicles;

The expected heights, velocity, duration, rate of rise, and sediment transport of the floodwaters and the effects of wave action, if applicable, expected at the site; and

The costs of providing governmental services during and after flood conditions including maintenance and repair of public utilities and facilities such as sewer, gas, electrical and water systems, and streets and bridges.

A written report addressing each of the above factors shall be submitted with the application for a floodplain variance.

Upon consideration of the factors listed above and the purposes of this ordinance, the appeal board may attach such conditions to the granting of floodplain variances as it deems necessary to further the purposes and objectives of this ordinance.

Any applicant to whom a floodplain variance is granted shall be given written notice specifying the difference between the Base Flood Elevation (BFE) and the elevation to which the structure is to be built and that such construction below the Base Flood Elevation increases risks to life and property, and that the issuance of a floodplain variance to construct a structure below the Base Flood Elevation will result in increased premium rates for flood insurance up to $25 per $100 of insurance coverage.  Such notification shall be maintained with a record of all floodplain variance actions, including justification for their issuance.

The Floodplain Administrator shall maintain the records of all appeal actions and report any floodplain variances to the Federal Emergency Management Agency and the State of North Carolina upon request.

Conditions for Floodplain Variances:

Variances shall not be issued when the floodplain variance will make the structure in violation of other Federal, State, or local laws, regulations, or ordinances.

Variances shall not be issued within any designated floodway or non-encroachment area if the floodplain variance would result in any increase in flood levels during the base flood discharge.

Variances shall only be issued upon a determination that the floodplain variance is the minimum necessary, considering the flood hazard, to afford relief.

Variances shall only be issued prior to development permit approval.

Variances shall only be issued upon:

A showing of good and sufficient cause;

A determination that failure to grant the floodplain variance would result in exceptional hardship; and

A determination that the granting of a floodplain variance will not result in increased flood heights, additional threats to public safety, or extraordinary public expense, create nuisance, cause fraud on or victimization of the public, or conflict with existing local laws or ordinances.

A variance may be issued for solid waste disposal facilities or sites, hazardous waste management facilities, salvage yards, and chemical storage facilities that are located in Special Flood Hazard Areas provided that all of the following conditions are met.

The use serves a critical need in the community.

No feasible location exists for the use outside the Special Flood Hazard Area.

The reference level of any structure is elevated or floodproofed to at least the regulatory flood protection elevation.
The use complies with all other applicable Federal, State and local laws.

The Town of China Grove has notified the Secretary of the North Carolina Department of Crime Control and Public Safety of its intention to grant a floodplain variance at least thirty (30) calendar days prior to granting the floodplain variance.


Section 16.8 Nonconformities

A nonconforming use shall not be changed to another nonconforming use nor shall a nonconforming structure be replaced after it has been destroyed except upon approval by the Board of Adjustment.

The Board of Adjustment may only grant a change in a nonconforming use or replacement of a nonconforming structure which has been destroyed after having first held a public hearing and having determined that:

Said change or replacement will be more suitable and appropriate for the lot(s) on which it is located than the existing situation, and
That the proposed change will have a less harmful effect than the existing situation on the properties surrounding the lot(s) in question, and
That the decision to grant the change will be in harmony with the general purpose and intent of this Ordinance and will not be injurious to the neighborhood or otherwise be detrimental to the public welfare.

The Board of Adjustment, in granting said changes, may prescribe appropriate conditions and safeguards in conformity with this Ordinance in order to conform with (B) above.  Violation of such conditions and safeguards when made a part of the terms upon which the change was granted, shall be deemed a violation of this Ordinance and shall be punishable as prescribed in Chapter 14 of this Ordinance.


Section 16.9 Rehearings and Appeals from the Board of Adjustment

An application for a rehearing shall be made in the same manner as provided for an original hearing within a period of 15 days after the date of the Board of Adjustment's decision.  In addition, specific information to enable the Board of Adjustment to determine whether or not there has been a substantial change in facts, evidence, or conditions in the case, shall be presented in writing or graphically.  A rehearing shall be denied by the Board of Adjustment, if, in its judgment, such change in facts, evidence or conditions has not been proven.  A public hearing shall not be required to be held by the Board of Adjustment to consider holding such a rehearing.  Approval of said consideration shall, however, require an affirmative vote of at least four (4) voting members.  In the event that the Board of Adjustment finds that a rehearing is warranted, it shall thereupon proceed as in the original hearing except that the application fee shall be waived.







Every decision of the Board of Adjustment shall be subject to review by the Superior Court Division of the General Courts of Justice of the State of North Carolina by proceedings in the nature of certiorari.  Any petition for review by the Superior Court shall be duly verified and filed with the Clerk of Superior Court within 30 days after the decision of the Board is filed in the office of the Zoning Administrator, or after a written copy thereof is delivered to every aggrieved party who has filed a written request for such copy with the Zoning Administrator or the Chairman of the Board of Adjustment at the time of the Board's hearing of the case, whichever is later.













