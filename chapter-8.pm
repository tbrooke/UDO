8.5.2 Design of Loading Spaces
Off-street loading spaces shall be designed and constructed so that all maneuvering to park vehicles for loading and unloading can take place entirely within the property lines of the premises.  Loading spaces must be designed so as to not interfere with the normal movement of vehicles and pedestrians on public rights-of-way.  Off-street loading spaces shall be located in the rear yard.  No area allocated to loading and unloading facilities may be used to satisfy the area requirements for off-street parking, nor shall any portion of any off-street parking area be used to satisfy the area requirements for loading and unloading facilities.

Each loading space shall have a paved surface and be a minimum of 12 feet in width and 40 feet in length.  Each such berth shall also have a minimum vertical clearance of 14 feet.


Section 8.6 Driveways

These driveway requirements do not apply to single family detached and two-family attached residential uses.

Driveways shall be not less than 10 feet in width for one-way traffic and 18 feet in width for two-way traffic.  

Twelve (12) foot wide driveways are permissible for two way traffic when:
The driveway is not longer than 50 feet; and
The driveway provides access to not more than six parking spaces; and
Sufficient turning space and stacking area is provided so that vehicles need not back into a public street.  

In no case shall a driveway width exceed 36 feet, except as required by NCDOT.

No surface parking or circulation driveway is permitted within any required or established setback, except that driveways providing access to the parking area may be installed across these areas.

Driveways shall be as nearly perpendicular to the street right-of-way as possible.

Driveways shall line up with other driveways across the street and be shared between adjacent uses wherever possible. 

No driveway on US Highway 29 or Highway 152 shall be less than 300 ft from an existing driveway, unless a shared driveway arrangement is not feasible, or other similar hardships as determined by the TRC.


Section 8.7 Lighting

8.7.1 Applicability
The provisions of this Section shall apply to the installation of all outdoor lighting within the jurisdiction of this ordinance except that the following shall be exempt from this Section.

 Outdoor lighting installed for governmental purposes by local, state or federal governmental units and their agents.

Outdoor lighting required to be installed by laws or regulations of a local, state or federal governmental units.

Outdoor lighting installed for one and two-family dwellings.

Lighting associated with temporary events such as holidays and which does not violate the basic principles of this Section.

Outdoor lighting associated with a bona fide farm operation as defined by NCGS 153A-340.



8.7.2 Prohibited Outdoor Lighting
The following types of outdoor lighting are prohibited unless exempt by Section 8.7.1 above:

Light fixtures that imitate an official highway or traffic control light or sign.

Light fixtures in the direct line of vision with any traffic control light or sign.

Light fixtures that have a flashing or intermittent pattern of illumination.

Privately owned light fixtures located in the public right-of-way.

Light fixtures that are a source of glare by their design, orientation or intensity.

Searchlights.

Light fixtures that violate any law of the State of North Carolina relative to outdoor lighting.

Structure highlighting.

Unshielded open vertical light fixtures.

8.7.3 Plan Approval Required
Outdoor lighting is subject to approval by the Zoning Administrator and Technical Review Committee (TRC).  The lighting plan shall show sufficient information to determine compliance with the standards of this Section.  In approving lighting plans the Zoning Administrator and TRC may modify the standards of this Section where the applicant agrees and where equal or better performance would result.  In granting modifications, the Zoning Administrator and TRC may require such conditions as will secure, insofar as practicable, the objectives of the requirements modified.

8.7.4 Outdoor Lighting General Provisions
Outdoor lighting fixtures shall be installed in a manner to protect the street and neighboring properties from direct glare or hazardous interference of any kind.  Glare or hazardous interference are situations where the sensation produced by luminance within the visual field is sufficiently greater than the luminance to which the eyes are adapted, such as to cause annoyance, discomfort, or loss in visual performance and visibility.

Outdoor lighting shall be constructed and operated to minimize the spill over of obtrusive light onto property outside the boundaries of the property on which the lighting is sited which could result in annoyance, discomfort or distraction to persons on the other property.  

All outdoor lighting fixtures, subject to this section, except for temporary and emergency lighting, that would otherwise cause glare or obtrusive spill over shall be shielded, recessed or otherwise oriented or treated in such a way to prevent glare or obtrusive spill over.  

As a general principle, all outdoor lighting shall be beamed downward and away from adjoining property and streets.  Upward lighting shall only be permitted as a modification by the Planning Board.

The maximum light level shall not exceed 0.5 maintained footcandles at any property line in a residential district, or on a lot occupied by a dwelling, congregate care or congregate living structure, and shall not exceed 2.0 maintained footcandles at any public street right-of-way.
All flood lights shall be installed such that the fixture shall be aimed down at least 45 degrees from vertical, or the front of the fixture is shielded such that no portion of the light bulb extends below the bottom edge of an external shield.  Flood lights and display lights shall be positioned such that any such fixture located within 50 feet of a public street right-of-way is mounted and aimed perpendicular to and away from the right-of-way, with a side-to-side horizontal aiming tolerance not to exceed 15 degrees from perpendicular to the right-of-way.

All flood lamps emitting 1,000 or more lumens shall be aimed at least 60 degrees down from horizontal, or be shielded such that the main beam from the light source is not visible from adjacent properties or the public street right-of-way.

All wall pack fixtures shall be cutoff fixtures.

8.7.5 Lighting Standards for Parking Lots and Outdoor Activity Areas
Other than flood lights and flood lamps, all outdoor area and parking lot lighting fixtures of more than 2,000 lumens shall be cutoff fixtures.

The mounting height of all outdoor lighting shall not exceed 40 feet above finished grade.

8.7.6 Lighting Standards for Vehicular Canopies
Areas under a vehicular canopy shall have a maximum point of horizontal illuminance of 24 maintained footcandles (FC).  Areas outside the vehicular canopy shall be regulated by the standards of 8.7.5 above.  Lighting under vehicular canopies shall be designed so as not to create glare off-site.  Acceptable methods include one or more of the following:

Recessed fixture incorporating a lens cover that is either recessed or flush with the bottom surface (ceiling) of the vehicular canopy.

Light fixture incorporating shields, or shielded by the edge of the vehicular canopy itself, so that light is restrained to five degrees or more below the horizontal plane.

Surface mounted fixture incorporating a flat glass that provides a cutoff fixture or shielded light distribution.

Surface mounted fixture, typically measuring two feet by two feet, with a lens cover that contains at least two (2) percent white fill diffusion material.

Indirect lighting where light is beamed upward and then reflected down from the underside of the vehicular canopy.  Such fixtures shall be shielded such that direct illumination is focused exclusively on the underside of the vehicular canopy.

Other methods approved by the Planning Board.

8.7.7 Lighting Standards - Outdoor Sports Field/Outdoor Performance Areas
The mounting height of outdoor sports field and outdoor performance area lighting fixtures shall not exceed 80 feet from finished grade.

All outdoor sports field and outdoor performance area lighting fixtures shall be equipped with a glare control package (louvers, shields, or similar devices).  The fixtures must be aimed so that their beams are directed and fall within the primary playing or performance area.

The hours of operation for the lighting system for any game or event shall not exceed one hour after the end of the event.

8.7.8 Lighting Standards for Outdoor Display Areas
Parking lot outdoor areas shall be illuminated in accordance with the requirements for 8.7.5 above.  Outdoor display areas shall meet the following standards:

Outdoor display areas shall have a maximum point of illuminance of 24 maintained footcandles (FC).

All light fixtures shall meet the IESNA definition of cutoff fixtures.  Forward throw fixtures (type IV light distribution, as defined by the IESNA) are required within 25 feet of any public street right-of-way.  Alternatively, directional fixtures (such as flood lights) may be used provided they shall be aimed and shielded in accordance with this Section.

The mounting height of outdoor display area fixtures shall not exceed 40 feet above finished grade.

8.7.9 Lighting Standards for Signs
Sign lighting shall be regulated in accordance with Chapter 11.

8.7.10 Lighting Standards for Buildings and Landscaping
Lighting fixtures shall be selected, located, aimed, and shielded so that direct illumination is focused exclusively on the building façade, plantings, and other intended site features and away from adjoining properties and the public street right-of-way.

8.7.11 Light Measurement Technique
Measurements shall be made at finished grade (ground level), with the light-registering portion of the meter held parallel to the ground pointing up.  The meter shall have cosine and color correction and have an accuracy tolerance of no greater than plus or minus five (5) percent.  Measurements shall be taken with a light meter that has been calibrated within the year.  Light levels are specified, calculated and measured in footcandles (FC).  All FC values herein are maintained footcandles.

8.7.12 Street Lighting Provisions
The Town of China Grove provides street lighting along public streets within the corporate limits.  All requests for street lighting installation and removal are to be submitted in writing to the Town Manager.  The following are general provisions for street lighting:

The Town of China Grove, as a minimum standard, shall install and maintain street lights in residential sections only at street intersections or terminations, unless the distance between street intersections exceeds 350 feet.  China Grove provides street lighting on existing publicly-dedicated streets in accordance with the following schedule:

Single Family Residential Areas:  100 Watt High Pressure Sodium (HPS) bulb mounted on 30 foot wood poles, 500 feet on-center
Multi-Family, Mixed Use and Commercial Areas:  250 Watt High Pressure Sodium (HPS) bulb mounted on 30 foot wood poles, 300 feet on-center

Consideration should be made to place all lighting at all intersections and other high pedestrian and other high pedestrian use areas.

Lighting should be planned to ensure adequate illumination for safety of both the pedestrian and the automobile.
Where sidewalks exist on one side of the street only, street lighting will be placed on that side to ensure adequate illumination for the pedestrian.

Additional lighting on a street with existing lighting should be similar in style and illumination to the existing lighting.

Citizens requesting lighting not on public street rights-of-way within China Grove will be referred to the appropriate electric utility.  The requesting party will be responsible for all costs associated with lighting non-public street rights-of-way or for which the Town has not authorized installation by the appropriate electric utility to install lighting.

8.7.13 Decorative Lighting
Decorative lighting is encouraged in developments in the R-M and N-C districts.  

Property owners associations for developments may choose a decorative light option upon obtaining lease agreement for the decorative light installation and maintenance with Duke Power.

Where underground wiring currently exists along thoroughfares and collector streets, the Town of China Grove may provide decorative pole fixtures at the Town’s expense, subject to the approval of the Town Council.

8.7.14 Standards for Acceptance of Lighting by Town
The Town of China Grove may choose to take over responsibility for payment of monthly billing of a street light, provided that the street light:

Is within Town-owned right-of-way, and
Is within a street right-of-way accepted for maintenance purposes by the Town of China Grove or the North Carolina Department of Transportation; and
Meets Town standards, or the petitioner has paid any cost to have the light brought up to Town standards

Takeover billing shall become effective for the billing cycle following the approval of the request.  The Town will not reimburse any billing for which the requesting party is responsible for or if the Town has not authorized Duke Power Company, to bill it.  This includes any billing between the date of the citizen’s request and the date of the changeover of billing.  Take-over billing is only applicable for maintenance and electricity billing only.  The Town will not accept any take-over billing of decorative lighting fixtures until all premium costs are paid in full.

The Town may accept street lighting that exceeds the Town’s standards for spacing and/or illumination subject to approval by the Town Council

Due to their low lighting capability and maintenance requirements, natural gas lights are not eligible for this program.  

8.7.15  Removal of Street Lighting
An individual homeowner or neighborhood organization may request removal of a street light provided the overall lighting pattern in the neighborhood is consistent with this Policy.  Requests to remove all lighting within a neighborhood shall require the unanimous consent of all property owners within the neighborhood and approval by the Town Council.


Section 8.8 Street Design Standards

8.8.1 Conformance with Thoroughfare Plan
The location and design of streets shall be in conformance with the adopted Metropolitan Planning Organization and China Grove Thoroughfare Plans.  Where conditions warrant, right-of-way width and pavement width in excess of the minimum street standards may be required. In any case where any part of a subdivision lies within the corridor of a Thoroughfare shown on a roadway corridor official Map adopted pursuant to NCGS Chapter 136, Article 2E, no subdivision approval shall be granted with respect to the property in the roadway corridor. Provided, however, no subdivision Plat approval shall be delayed by the provision of the Roadway corridor official map procedure for more than three (3) years from the date of its original submittal. 

8.8.2 Street Classification 
Diagrams depicting each street category and specifications are located in Appendix B of this Ordinance.

Category 3:  Thoroughfares that connect town and regional centers.  Category 3 streets are typically state-maintained roads that have some level limited access to surrounding property and operate at higher speeds.

Category 2:  Connects neighborhoods to commercial centers.  Category 2 streets operate at low to moderate speeds and may have on-street parking.

Category 1:  Typically provide access to residential areas and connect neighborhoods.  Category 1 streets focus on low traffic volume, high accessibility, pedestrian and bicycle safety, and utility infrastructure for neighborhoods.

Alleys:  Privately maintained right-or-way to access the rear of buildings.    

8.8.3 Connectivity
The planned street layout of a proposed development shall be compatible with and connect to existing or proposed streets.

The street network of a new development shall stub to adjacent properties with development potential whenever possible and where topographic conditions allow at the discretion of the Technical Review Committee and the Planning Board.  Where a stub abuts a stream, one-half (1/2) the cost of a bridge to cross the stream shall be bonded for five (5) years to allow for connection to future development across the stream.  Development across the stream from an existing stub shall incur the one-half (1/2) the cost of the bridge and have responsibility for the construction of the bridge. 

Cul-de-sacs shall not be used to avoid connection with an existing street, to avoid the extension of a thoroughfare or collector Street, or to avoid connection to adjoining property.  Permanent cul-de-sac streets should not exceed 400 feet in length unless necessitated by topography or property accessibility and approved by the Planning Board. Measurement shall be from the point where the centerline of the street intersects with the centerline of a through street to the center of the turnaround of the cul-de-sac.





For consideration of a major subdivision, the plan must meet the following street connectivity requirements of the NC Fire Code, Appendix D, Section D107.1:

Must provide at least two (2) road accesses to a Collector Road (see Category 2 Collector Road specifications B.2.2 Street Design Criteria by Type) for all major subdivisions of 30 or more dwelling units. 
Must provide vehicle access, where public access is possible to all adjacent existing subdivisions.
Must provide stub streets to the property lines of all adjacent vacant or under-developed property.  

8.8.4 Reserve Strips and Half Streets
Reserve strips and non-access easements adjoining street rights-of-way for the purpose of preventing access to or from adjacent property, (except those required to prevent access to Thoroughfares) and half-streets shall not be permitted under any condition. 

8.8.5 Private Streets & Gated Communities
Private streets and gated communities are not permitted within the jurisdiction of China Grove.  However, privately maintained alleys that meet the provisions of this Section are permitted.

8.8.6 Intersections 
Streets shall be designed so as to intersect as nearly as possible at right angles, and no street shall intersect any other street at an angle of less than 60 degrees. Streets crossing natural areas or streams shall cross at or near to right angles as possible within limits of topographic conditions. Offset intersections are to be avoided.  Intersection offsets shall meet the standards set forth in Appendix B. 
 
8.8.7 Marginal Access Streets 
Where a tract of land to be subdivided adjoins a Thoroughfare, the developer may be required to provide a marginal access street parallel to the Thoroughfare or provide for through lots on a local street for the lots to be developed adjacent to the Thoroughfare. Where through lots are established, such lots may be prevented from having direct access to the Thoroughfare by driveways through the use of reserve strips or non-access easements. 

8.8.8 Utilities, Street Lights and Storm Drainage within Streets
Utilities, street lights, sidewalks, storm drainage and other such facilities to he placed within the street right-of-way shall be placed in accordance with Town standards as shown in Appendix B.  All utilities shall be placed underground. 

8.8.9 Pavement, Curb and Gutter and Pavement Side Ditch
Pavement, curb and gutter to be placed in public streets shall be placed in accordance with Town Standards as shown in Appendix B. 

8.8.10 Connection to State Streets
An approved permit is required to connect to any existing state system street.  NCGS 136-102.6 "Compliance of Subdivision streets with minimum standards of the Board of Transportation Required of Developers" requires that new public streets outside the Town limits and changes to existing streets inside the Town limits that are the responsibility of NCDOT be in accordance with the minimum right-of -way and construction standards established by the Board of Transportation for acceptance on the state highway system. It is the intent of these standards and requirements, as set forth, to complement and not to conflict with the requirements of NCDOT as stated in NCGS 136-102.6.  In all cases the most restrictive limitation or requirement or the requirement causing the highest standard of improvement shall govern. 

All proposed streets shall be constructed in accordance with the minimum Public street standards as shown in Appendix B.  All street improvements shall be designed and installed in accordance with Town Standards and the approved Construction Plan. The developer’s engineer shall furnish the Town with a certified statement that all street improvements installed in the subdivision meet the minimum standards of the Ordinance. 

In addition, street improvements, shall be installed in the following situations: 

Any existing street segment that has not been accepted for maintenance by either the Town or the NCDOT, and that is to serve as the required frontage for one or more Lots created pursuant to this ordinance, shall be improved and Dedicated to the public, as provided for above, in such a way that the Street segment meets the standards of this ordinance for the particular classification of Street, including right-of-way width.  Such street segment shall be directly connected to the existing public street system by way of at least one public street accepted for maintenance by either the Town or the NCDOT. No subdivision shall be permitted on any street that is an "island" not connected directly to the public street system. 

Subdivisions that adjoin existing streets maintained by either the Town or NCDOT shall dedicate additional street right-of-way necessary to meet the minimum width requirements for the type of classification of the adjoining street. When any part of the subdivision is on both sides of an existing Street, the entire minimum right-of-way shall be provided. When the subdivision is located on only one side of an existing street, one-half of the minimum right-of-way, measured from the centerline of the existing street, shall be provided.  The improvement requirements of this Article shall not apply to the subdivision of lots fronting on established streets that have already been accepted for maintenance by the Town or NCDOT. 

The Planning Board may require pavement and widening or pavement and widening and curb and gutter for turning lanes along any existing or proposed street that forms a significant entrance to a proposed subdivision where in the opinion of the Board such improvements are necessary in order to provide for safe vehicular movement into and out of the proposed Subdivision. 

In cases where a street is stubbed into adjoining property for future extension and such street serves as the frontage for one or more lots which are not corner lots, the Planning Board may require the pavement of a temporary turn-around in a form similar to a cul-de-sac on such street where in their opinion such turn-around is necessary for the public convenience, safety and service. Temporary easements for such purposes may be required. 


Section 8.9 Sidewalks

Sidewalks shall be installed within the street right-of-way constructed in accordance with Town Standards as outlined in Appendix B, in the following situations: 

In any case where a development adjoins and has legal access to, or will have legal access to, an existing or proposed major or minor Thoroughfare as shown on the Thoroughfare Plan, the developer shall construct a sidewalk along the frontage of such Thoroughfare. 

In any case where a development adjoins a street or will adjoin an extension of a street, which has sidewalk on one or both sides within 500 feet of the boundary of the land to be developed, the sidewalk shall be constructed along the adjoining frontage of such street in such a way that the existing sidewalk pattern will be extended. 

In any case where a development creates new public streets, the developer shall construct a system of sidewalks on both sides of the street. 


Section 8.10 Water and Sewer

8.10.1 Connection to System Required 
Any development which has Town water and/or sewer system lines available shall be required to extend the public water and/or sewer system throughout the development to each lot located therein. All required line extensions shall include appropriate valves, hydrants, taps, service, manholes, lift stations, pumps, and clean outs to the property line of each lot as required by Town standards.

In any case where a public water and/or sewer system intended to serve more than two (2) lots is proposed to be installed in a development as part of the plan approval process, such system shall be considered to be a required improvement within the context of this Section regardless of whether such a system is an extension of the Town system or not and such system shall be required to be installed by the developer.  This requirement includes both facilities within the development and off-site facilities which are essential to providing the service to the property. 

Where public sanitary water and sewer are not available as defined below in 8.10.2, structures shall be connected to private water supply and sewage disposal systems (i.e. wells and septic tanks or community systems).

8.10.2 Availability Defined
For developments within or partially within the Town, the term "available”, shall mean that there is an existing line of adequate size and flow and/or pressure either crossing the development property or immediately available from an adjacent public right-of-way or the Town indicates its commitment to extend such a line to the property line of the development at no cost to the developer. 

