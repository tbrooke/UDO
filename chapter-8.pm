Section 8.1 Parking General Provisions

The purpose of this section is to ensure that adequate and well-designed parking is provided for developments in the Town of China Grove.  The following are general requirements for all new developments except single-family detached residential and two-family attached residential (duplexes).  The expansion of existing development shall follow these requirements to the greatest extent possible.

Except in the Highway Business, Light Industrial, and Heavy Industrial districts, off-street parking areas shall be located to the side or rear of buildings.  Off-street parking areas in the side yards shall not extend beyond the frontage line of the building.  Off-street parking areas in the Highway Business district may be located in the front yard.  However, the parking should not be prominent and should be screened from view with buildings and landscaping wherever possible.

All side and rear yard off-street parking areas shall be screened from view in accordance with Type B landscaping in accordance with Chapter 7.

Off-street parking areas shall be designed to facilitate adequate movement and access by sanitation, emergency, and other public service vehicles.

Off-street parking areas shall be designed so that parked vehicles do not encroach upon, extend onto, or cause vehicles to back into public rights-of-way, sidewalks or strike against or damage any wall, vegetation, utility, or other structure.

Off-street parking areas of greater than five (5) spaces may be required to provide curb and gutter in accordance with Stormwater Manual as referenced in Section 8.11.6.

The size of any single surface parking lot shall be limited to three (3) acres, unless divided by a street or building.  Larger parking lots shall be separated by buildings or landscaped areas.

The following shall be paved or contain a similar type material approved by the Zoning Administrator.  Gravel and other stabilization material without a permanent wearing surface is not permitted:
Front yard parking areas.
Side yard parking areas.
All off-street parking areas for lots of greater than one acre.
Driveways

Off-street rear yard parking areas for lots of less than one acre may use pea gravel in lieu of a paving material provided that handicap parking meets ADA standards and pea gravel is contained to the parking area using landscaping timbers or other containment device. 

Paved parking areas shall have lines demarcating each parking space.

Off-street parking areas shall be properly maintained in all respects.  In particular, and without limiting the foregoing, off-street parking area surfaces shall be kept in good condition (free from potholes, etc.) and parking space lines or markings shall be kept clearly visible and distinct.

On-street parking may be used to satisfy parking requirements on streets.

A secure bicycle rack is required for all parking lots greater than 50 spaces. Bicycle parking may be placed in the front yard.


