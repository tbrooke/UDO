Section 8.3 Shared Parking and Parking Connectivity

8.3.1 Shared Parking
The joint use of shared off-street parking between two uses may be made by contract by two or more adjacent property owners.  Developments that operate at different times may jointly use or share the same parking spaces with a maximum of one-half (1/2) of the parking spaces credited to both uses if one use is a church, theater, assembly hall or other use whose peak hours of attendance will be at night or on Sundays, and the other use will be closed at night or on Sundays.



8.3.2 Parking Connectivity
Adjacent parking lots shall be interconnected except in the case of existing steep topography between the sites.  Each parking area that is interconnected may reduce their minimum parking requirement by five (5) percent.  


