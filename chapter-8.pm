8.10.3 Exemption from Extension of Lines
In the event the Town elects not to extend a line of sufficient size, flow and/or pressure, to the development (if in the Town) or within 400 feet of the development boundary (if outside the Town) because of topographic features, legal obstacles, or financial reasons, then, the developer shall not be required to extend water lines to each lot nor provide water and/or sewer service to the development. 

8.10.4 Oversized Water and Sewer Facilities
The Town may, in order to serve future development, require the developer to install certain oversized water and sewer improvements and/or to increase such improvements to a size and/or extent beyond that necessary for the needs created by the subdivision. In such cases, the Town shall enter into an agreement to reimburse the developer for the oversizing and/or extension based upon rates as agreed to by the Town. 

8.10.5 Annexation Required
In any case where a new development connects to the Town water and/or sewer system, such development shall be voluntarily annexed into the Town prior to the approval of a final plat, for a subdivision, or the issuance of a Zoning Permit, where the subdivision of land is not involved.

8.10.6 Installation Requirements
All water and sewer extensions for new development inside or outside the corporate limits will be made by the town's public works department or approved licensed contractors.

No water or sewer line may be connected to the system of the Town unless such line is properly designed and constructed to service the properties intended to be served directly by such line and of a size and design sufficient to accommodate any necessary expansion of the water and sewer system to serve other properties, including fire protection.

The Town shall own and control any and all water and sewer lines and related facilities connected to and serviced by its water or sewer system, except those lines and facilities of other public bodies connected to and serviced by the Town’s water or sewer system under contracts approved by the Town Council between the Town and other public bodies. 

Because the extension of water or sewer lines to certain properties benefits the owners of such properties by raising property values, the cost of such extension shall be borne by the developers of such properties as provided in this article except in instances when the Town Council makes a determination that the Town is obligated to extend such utilities when it determines that it is the best interest of the Town to do so.  In making such a conclusion, it must be demonstrated by the developer to the Town Council that ad valorem taxes to be gained by the Town from the properties which will be served by the proposed utilities will over a five (5) year period exceed the cost incurred by the Town for making such utility extensions.  

Additional requirements regarding sizes and installation methods are located in Appendix B. 

8.10.7 Extensions Required by State Law and Emergency Situations
To comply with municipal obligations by state statutes, or in cases of emergency where it is found to be in the public interest or necessary to protect the public health, the Town may authorize extensions of water or sanitary sewer into specific areas.

8.10.8 Payment of Tap Fees
Nothing in this article shall exempt an applicant from paying the standard tap fees in effect at the time the application for connection is made as adopted from time to time by the Town Council.

8.10.9 Extensions to Existing Development
Extensions of water or sanitary sewer service within the corporate limits of the town shall be made upon petition as set forth by the Town Council in the Code of Ordinances.

8.10.10 Extensions to New Development
Any person desiring to install any water or sewer line for new developments within the town's service area to be connected to and served by the water and sewer system of the town shall make application on forms provided by the Town, and shall furnish such information or exhibits as are required by such application forms. 

Such application for extension, whenever possible, shall be made simultaneously with the appropriate Development Review Process for the type of development proposed as outlined in Chapter 9.

The applicant shall pay to the town a nonrefundable application fee.  This fee shall be equal to the annual average water and sewer extension engineering fee, as determined by the board for the July 1 to June 30 fiscal year, plus an additional fee as established by the board from time to time. 

The applicant shall submit engineering plans, profiles and specifications for such water main or sewer line, including those for any required fire hydrants, valves, manholes, sewer lift stations, force mains or other sewer lines necessary in connection therewith, to the town engineer for approval by the board of aldermen.  All plans shall bear the seal of a registered professional engineer.

No water or sewer line may be installed and connected to the town water or sewer system except as approved by the Town Council.  Such approval shall meet the requirements of the Code of Ordinances as adopted by the Town Council.

8.10.11 Denial of Extension
The Town Council will not approve any contract for the installation of any water main or sewer line to be connected to and served by its water or sewer system if in its judgment the projected volume of water that would be used by any properties to be serviced thereby would unduly tax the available water supply or sewage treatment capacity of the town, or it would not be feasible or otherwise suitable for the Town to commit itself to such cost.  Exceptions to this policy may be made by the Town Council for any meritorious reason and good cause shown.


Section 8.11 Stormwater Management

8.11.1 Comprehensive Storm Drainage Plan
A comprehensive storm drainage system shall be planned and implemented for each development of greater than 20,000 square feet of impervious area in accordance with the general standards and requirements of this Section.  Detail plans where required shall be submitted as part of the Construction Documents. 

Storm drainage plans shall be considered on an individual basis depending upon the situation within a given development. 

The requirements of the Flood Damage Prevention ordinance shall apply to storm drainage design where applicable.

