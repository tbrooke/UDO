8.11.5 Adoption of Standards by Reference
The Town Council hereby finds that hydrologic conditions in Rowan County and Mecklenburg County are similar and that it is in the public interest to maintain a uniform regional procedure for computing the stormwater impacts of new development.  Accordingly, the design of stormwater management facilities shall be computed in accordance with the most current version of the Charlotte-Mecklenburg Storm Water Design Manual (hereinafter “Stormwater Manual”).




8.11.6 Obstruction of Drainage Channels Prohibited
No fences or structures shall be constructed across an open drainage channel that will reduce or restrict the flow of water.  The Zoning Administrator may require any water course or stormwater management facility to be located within dedicated a drainage easement officially recorded at the Rowan County Register of Deeds as a “permanent drainage easement” that provides sufficient width for maintenance.

8.11.7 Grading Standards
The following standards shall be followed in establishing the grading plan for a development:

Developments shall be designed and constructed with a positive drainage flow away from buildings towards approved stormwater management facilities.  Plans for drainage facilities shall be approved by the Town Engineer. 

In the design of site grading plans, all impervious surfaces in the proposed development (including off street parking) shall be considered.

Site grading and drainage facilities shall protect sinkholes, wetlands, ponds and lakes from increased sediment loading.

Site grading shall not increase runoff flow rate onto downstream properties.

A site shall not be graded to create a slope greater than 2:1 at adjacent properties unless the slope already existed on the undeveloped land.  In no case shall the slope be made steeper than it was on the undeveloped land.  Terraced or stepped slopes that achieve the overall slope requirement may be considered by the TRC on a case by case basis.

All disturbed areas within the dedicated right‑of‑way and easements of any subdivision street shall be restored with vegetation and the landscaping standards of Chapter 7 shall be met.

Stormwater facilities to be located in designated open space areas shall be regulated in accordance with Chapter 6 of this Ordinance.

