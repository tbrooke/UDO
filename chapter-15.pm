Section 15.1 Authority and Duties

The Planning Board shall have the following duties and responsibilities:

To review and approve or deny requests for Major Subdivision Plans and Major Site Plans.

To review, hold “courtesy hearings”, and make recommendations for Conditional Use Permits, Vested Rights Plans, Text Amendments, Map Amendments (Standard and Conditional Use Rezonings).

To render opinions and make recommendations on all issues related to the Unified Development Ordinance, Zoning Map, Comprehensive Plan, and other land use plans which may be adopted from time to time and which require approval by the Town Council.


Section 15.2 Membership

The Planning Board shall consist of a total of nine (9) members.  Representation shall be provided by appointing at least two (2) residents of the extraterritorial jurisdiction. The total membership of the Planning Board shall include a diverse geographical representation overall.  Representatives from within the Town limits shall be appointed by the China Grove Town Council. Representatives from the ETJ area shall be appointed by the Rowan County Board of Commissioners.  All members shall serve for overlapping terms of three (3) years.


Section 15.3 Meetings, Hearings, and Procedures

All meetings and hearings shall be open to the public and shall be conducted in accordance with the procedure set forth in these regulations and rules of procedure adopted by the Planning Board.  Such rules of procedures may be amended by the Planning Board membership.

Any rules of procedure adopted by the Planning Board shall be kept on file at the offices of the Zoning Administrator and shall be made available to the public at any meeting or hearing of the Planning Board.


Section 15.4 Staff

The Zoning Administrator or his/her designee shall serve as staff to the Planning Board and shall provide technical assistance to the Planning Board as requested.




















