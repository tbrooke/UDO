Section 3.1 ◊a[#:name "Purpose Statement and Interpretation"]{Purpose Statement and Interpretation}

Section 3.1.1 Purpose Statement

In accordance with the requirement of NCGS 160A-382 that zoning regulation be by districts, the Town, as shown on the Zoning Map accompanying this Ordinance, is hereby divided into the following districts which shall be governed by all of the uniform use and dimensional requirements of this Ordinance.

The purposes of establishing the following zoning districts are:

To implement adopted plans;

To promote public health, safety, and general welfare;

To provide for orderly growth and development;

To provide for the efficient use of resources;

To facilitate the adequate provision of services.

Section 3.1.2 Interpretation

Each zoning district has uses permitted by right, uses permitted with special requirements, and conditional uses.  Tables are shown for each district placing uses under one of the three categories.  In addition to the individual district tables, is a detailed permitted uses table showing the uses allowed in each district.  The following describes the processes of each of the three categories that the uses are subject to:

Permitted by Right:  Administrative review and approval subject to district provisions and other applicable requirements only.

Permitted with Special Requirements:  Administrative review and approval subject to district provisions, other applicable requirements, and special requirements outlined in Chapter 4.

Conditional Uses:  Administrative review and recommendation, Planning Board review and recommendation, Town Council review and approval of Conditional Use Permit subject to district provisions, other applicable requirements, and conditions of approval.  Some Conditional Uses may also be subject to special requirements outlined in Chapter 4. 



