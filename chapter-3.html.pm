3.16.21 Standards for Floodplains without Established Base Flood Elevations   

Within the Special Flood Hazard Areas designated as Approximate Zone A and established in 3.18.6, where no Base Flood Elevation (BFE) data has been provided by FEMA, the following provisions, in addition to the provisions of 3.18.18, shall apply:

No encroachments, including fill, new construction, substantial improvements or new development shall be permitted within a distance of twenty (20) feet each side from top of bank or five times the width of the stream, whichever is greater, unless certification with supporting technical data by a registered professional engineer is provided demonstrating that such encroachments shall not result in any increase in flood levels during the occurrence of the base flood discharge.

The BFE used in determining the regulatory flood protection elevation shall be determined based on the following criteria:

When Base Flood Elevation (BFE) data is available from other sources, all new construction and substantial improvements within such areas shall also comply with all applicable provisions of this ordinance and shall be elevated or floodproofed in accordance with standards in 3.18.18 and 19.

When floodway or nonencroachment data is available from a Federal, State, or other source, all new construction and substantial improvements within floodway and nonencroachment areas shall also comply with the requirements of 3.18.19 and 23.  

All subdivision, manufactured home park and other development proposals shall provide Base Flood Elevation (BFE) data if development is greater than five (5) acres or has more than twenty (20) lots/manufactured home sites.  Such BFE data shall be adopted by reference in accordance with 3.18.6 and utilized in implementing this ordinance.

When Base Flood Elevation (BFE) data is not available from a Federal, State, or other source as outlined above, the reference level shall be elevated or floodproofed (nonresidential) to or above the regulatory flood protection elevation, as defined in Appendix A. All other applicable provisions of 3.18.19 shall also apply.

3.18.22 Standards for Riverine Floodplains with Base Flood Elevations but without Floodways or Non-Encroachment Areas
Along rivers and streams where BFE data is provided by FEMA or is available from another source but neither floodway nor non-encroachment areas are identified for a Special Flood Hazard Area on the FIRM or in the FIS report, the following requirements shall apply to all development within such areas:

Standards of 3.18.18 and 19; and

Until a regulatory floodway or non-encroachment area is designated, no encroachments, including fill, new construction, substantial improvements, or other development, shall be permitted unless certification with supporting technical data by a registered professional engineer is provided demonstrating that the cumulative effect of the proposed development, when combined with all other existing and anticipated development, will not increase the water surface elevation of the base flood more than one (1) foot at any point within the community.




3.18.23 Floodways and Non-Encroachment Areas 

Areas designated as floodways or non-encroachment areas are located within the Special Flood Hazard Areas established in 3.18.6.  The floodways and non-encroachment areas are extremely hazardous areas due to the velocity of floodwaters that have erosion potential and carry debris and potential projectiles.  The following provisions, in addition to standards outlined in Article 5, Sections A and B, shall apply to all development within such areas:

No encroachments, including fill, new construction, substantial improvements and other developments shall be permitted unless:

it is demonstrated that the proposed encroachment would not result in any increase in the flood levels during the occurrence of the base flood, based on hydrologic and hydraulic analyses performed in accordance with standard engineering practice and presented to the Floodplain Administrator prior to issuance of floodplain development permit, or

A Conditional Letter of Map Revision (CLOMR) has been approved by FEMA.  A Letter of Map Revision (LOMR) must also be obtained upon completion of the proposed encroachment.

If 3.18.23.A is satisfied, all development shall comply with all applicable flood hazard reduction provisions of this ordinance.

No manufactured homes shall be permitted, except replacement manufactured homes in an existing manufactured home park or subdivision, provided the following provisions are met:

The anchoring and the elevation standards of 3.18.19.C; and

The no encroachment standard of 3.18.23.A.

3.18.24 Effect on Rights and Liabilities under the Existing Flood Damage Prevention Ordinance
This section in part comes forward by re-enactment of some of the provisions of the Flood Damage Prevention Ordinance enacted on April 7, 1987 as amended, and it is not the intention to repeal but rather to re-enact and continue to enforce without interruption of such existing provisions, so that all rights and liabilities that have accrued thereunder are reserved and may be enforced.  The enactment of this section shall not affect any action, suit, or proceeding instituted or pending.  All provisions of the Flood Damage Prevention Ordinance of The Town of China Grove enacted on April 7, 1987, as amended, which are not reenacted herein are repealed.  

3.18.25 Effect upon Outstanding Floodplain Development Permits

Nothing herein contained shall require any change in the plans, construction, size, or designated use of any development or any part thereof for which a floodplain development permit has been granted by the Floodplain Administrator or his or here authorized agents before the time of passage of this ordinance; provided, however, that when construction is not begun under such outstanding permit within a period of six (6) months subsequent to the date of issuance of the outstanding permit, construction or use shall be in conformity with the provisions of this ordinance.   



