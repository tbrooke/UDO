Section 3.15 ◊a[#:name "Conditional Use District (CU)"]{Conditional Use District (CU)}

3.15.1 Intent
       
There is a Conditional Use District (CU) which corresponds to each of the primary districts authorized in this ordinance.  It is recognized that certain types of zoning districts would be inappropriate at particular locations in the absence of special conditions.  The Conditional Use District is a means by which special conditions can be imposed in the furtherance of the purpose of this Ordinance.  The Conditional Use District classification will be considered for rezoning only upon request of a property owner with the presentation of a site specific plan.  

3.15.2 Uses and Other Requirements

Within a Conditional Use District, only those uses authorized as permitted or conditional uses in the zoning district with which the district corresponds shall be permitted, and all other requirements of the corresponding district shall be met as minimum standards.  Additional limitations on use and other requirements may be added as conditions by the Town Council.

See Chapter 13 for Conditional Use District Rezoning provisions.



Section 3.16 ◊a[#:name "Watershed Overlay District"]{Watershed Overlay District}

3.16.1 Purpose and Applicability

The purpose of the Watershed Overlay District is to provide for protection of the Lake Fisher and Lake Corriher water supplies consistent with the WS‑IV Protected Area management rules as adopted by the North Carolina Department of Environment and Natural Resources for such classified areas.

3.16.2 General Development Standards

The construction of new roads and bridges and non‑residential development should minimize built‑upon area, divert storm water away from surface water supply waters as much as possible, and employ best management practices (BMPs) to minimize water quality impacts.  The Department of Transportation BMPs as outlined in their document entitled "Best Management Practices for the Protection of Surface Waters" shall be used in all road and bridge construction projects in the Watershed Overlay District.

All development activities within the Watershed Overlay District, in addition those activities specifically regulated by these provisions, are subject to the standards, usage conditions and other regulations contained in the Rules and Requirements of the Surface Water Supply Protection Rules adopted by the North Carolina Department of Environment and Natural Resources.

A minimum 30‑foot vegetative buffer for development activities is required along all perennial waters, including streams, rivers and impoundments, indicated on the most recent versions of USGS 1: 24,000 scale topographic maps; provided, that nothing in this subsection shall prevent artificial streambank or shoreline stabilization.  No new development is allowed in the buffer, except that water dependent structures, or other structures such as flag poles, signs and security  lights, which result in only diminutive increase in impervious area and  public works projects such as road crossings and greenways may be allowed where no practicable alternative exists.  These activities shall minimize built‑upon surface area, direct runoff away from the surface water, and maximize the utilization of BMPs.

Where otherwise permitted in the underlying Primary Zoning District, Cluster Development is allowed on a project by project basis as follows:

The overall density of the project meets the density requirements of this Ordinance;
The appropriate vegetative buffer in (c) above is provided;
Built upon areas are designed and located to minimize storm water runoff impact to the receiving waters, minimize concentrated storm water flow, and maximize the flow through vegetated areas;
Areas of concentrated development are located in upland areas and away, to the maximum extent practicable, from surface waters and drainage ways;
Remainder of tract to remain in vegetated or natural state;
The area in the vegetated or natural state may be conveyed to a property owners association; a local government for preservation as a park or greenway; a conservation organization; or placed in a permanent conservation or farmland preservation easement.  A maintenance agreement shall be filed with the property deeds; and
Cluster development shall transport storm water runoff from the development by vegetated conveyances to the maximum extent practicable.

All development in Watershed Overlay Districts, shall, to the maximum extent practicable, minimize built‑up surface area, direct storm water runoff away from surface waters and incorporate best management practices to minimize water quality impacts.
Existing development, as defined in this Ordinance, is not subject to the requirements of the overlay provisions.  Expansions to structures classified as existing development must meet the requirements of these provisions, provided however, the built‑upon area of the existing development is not required to be included in the density calculations.  In determining expansions to existing development, the maximum permitted additional built-upon area is derived by multiplying the area of the portion of the property that is not built-upon by the appropriate percent built-upon limitation for the Overlay District in which the property is located.

A pre‑existing lot created prior to the effective date of this Ordinance, regardless of whether or not a vested right has been established, may be developed or redeveloped for single family residential purposes without being subject to the restrictions of these overlay provisions.

Any existing building or built‑upon area not in conformance with the limitations of these provisions that has been damaged or removed for any reason may be repaired and/or reconstructed, provided:

Repair or reconstruction is initiated within twelve (12) months and completed within two (2) years of such damage or removal.
The total amount of space devoted to built‑upon area may not be increased.
The repair or reconstruction is otherwise permitted under the provisions of this Ordinance.

No activity, situation, structure or land use shall be permitted or allowed to operate within a watershed which poses a threat to water quality and the public health, safety and welfare.  Such conditions may arise from inadequate on‑site sewage systems which utilize ground absorption; inadequate sedimentation and erosion control measures; the improper storage or disposal of junk, trash or other refuse within a buffer area; the absence or improper implementation of a spill containment plan for toxic and hazardous materials; the improper management of storm water runoff; or any other situation found to pose a threat to water quality.

The Zoning Administrator may require such information on Zoning Permit applications and site plans, including density/built‑upon area calculations, as he may deem necessary to determine compliance with the Watershed Overlay District provisions.

The Zoning Administrator may, prior to the authorization of any permit in the Watershed Overlay District, require evidence of a valid Sedimentation/Erosion Control Permit issued by the State of North Carolina or evidence satisfactory to the Administrator that no permit is required.

The Zoning Administrator shall maintain records of the administration of the Watershed Overlay District regulations and shall submit any modifications of the regulations to the Division of Water Quality of the North Carolina Department of Environment and Natural Resources.  The Zoning Administrator shall also maintain a record of variances issued and shall submit an annual report of each project receiving a variance and the reason for the variance to the Division of Water Quality.  The annual report shall contain the record of each variance granted by the Board of Adjustment during the previous calendar year and shall be submitted on or before January 1 of the following year.

