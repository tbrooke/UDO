Section 3.17 Planned Unit Development (PUD)

3.17.1 Purpose

The purpose of the Planned Unit Development district (PUD) is to provide for the orderly development of land with a mix of land uses and intensity.  PUD zoning is intended to permit flexibility in the design, construction and processing of residential and non-residential developments of a quality that could not be achieved under conventional zoning approaches.  While the conventional zoning districts and the requirements of those districts set forth in the UDO are reasonable, there may be circumstances in which it is in the community’s best interest to allow unique and/or creative designs and techniques that:  

Promote the most appropriate use of a parcel
Allow diversification of use
Facilitate the adequate and economical provisions of streets, parks, open space, schools, storm drainage and sewer and water utilities
Preserve and utilize open space
Offer recreational opportunities close to residential uses
Enhance neighborhood appearance

3.17.2 Processing Procedure 
A PUD shall be considered a conditional zoning district and shall be processed in accordance with § 9.2.3 of this Ordinance.

3.17.3 Permitted Uses 
The uses permitted in a PUD shall be the permitted uses as set forth in the approved site plan.

The site plan shall designate land use categories consistent with the zoning district classification of this Ordinance.  Within each land use category, proposed uses shall be subject only to the permitted uses in Table 3.14 for each land use category.  No conditional use permit shall be required for any conditional use listed for said land use category in Table 3.14 separate from that issued for the PUD itself.  

3.17.4 Land Use Composition
No site plan for a PUD district shall be approved unless the following percentages of land uses are provided for within the boundaries of the district. 

Moderate Density residential (2.5-7 units per acre) = 0-50% of total acreage
High Density residential (8 or more units per acre) = 0-50% of total acreage
Open space shall be required in accordance with § 6 of this Ordinance

3.17.5 Design Standards

The land uses within a PUD shall not be subject to any of the dimension and density provisions of Chapters 3 and 7 except that a perimeter setback of 25 feet shall be maintained from existing development.  

PUD designs shall be subject to the recommended design elements for Chapter 5.  The design elements are for consideration in the design of a Planned Unit Development and shall be considered as criteria for approval.  This is not to state that all of the design elements shall be included in a PUD, rather all elements shall be considered and those that are considered appropriate and reasonable should be included.  Additional design requirements are included in 3.17.8 below.  

3.17.6 Professional Design Team Required

An applicant for a PUD approval shall certify, in writing at the time of application, that a member of each of the following professions will be used in the planning and design process for the proposed development:

Project planning and design by a licensed North Carolina architect, licensed North Carolina landscape architect, or a registered land surveyor;
Landscaping design by a certified nursery or licensed North Carolina landscape architect; and 
Site engineering by a North Carolina Registered Engineer

3.17.7 Modification of Approved Final Site Plan

Following approval of the CZ district and the Conditional Use Permit, no modifications of the land use category designations, design standards, uses, densities or any other condition of the site plan shall be permitted unless a new Conditional Use Permit is approved.  However, the Administrator may approve the following modifications in writing without a new site plan: 

A change in the location of not more than ten percent (10%) of the dwelling units of floor area.  
A change in the location of any part of open space acreage of not more than ten percent (10%) of the gross acreage
A change in the location of any part of proposed street alignment and lot configuration of not more than ten percent (10%) of the gross acreage
An increase or decrease of any setback by not more than five (5) feet for setback of less than fifty (50) feet

3.17.8 Recommended Design Elements

As indicated in § 3.17.1, PUD zoning is intended to permit flexibility in the design, construction and processing of residential, commercial and/or industrial developments of a quality that could not be achieved under conventional zoning concepts.  Therefore, the following elements are recommended for consideration in the design of a Planned Unit Development and shall be included in a PUD, rather all elements shall be considered and those that are considered appropriate and reasonable should be included.  

Architectural Elements 

Building height, rhythm, articulation, massing and bulk are compatible with the individual site attributes and are compatible with the surrounding neighborhoods.  
Distinctive architectural details such as covered front porches, door and window details, roof overhangs, and/or parapet walls with cap features shall be provided on each dwelling or principal structure.  A variety of roofing colors, textures, and component shapes including shake shingle, shale, and wood components, should be provided.  
Significant architectural differences in the choice of elevations, roof lines, and exterior colors foe each residential floor should be provided.  Front façades shall not be identical to any house less than three (3) adjacent homes, nor shall the same rear façade which is visible from arterial street view, on any block front, be identical to any rear façade closer than three (3) adjacent homes.  Homes facing one another (across the street) shall not have the same façade.  
Residential design guidelines are provided, which include a variety of conceptual standard plans, and may include: variation in building setbacks, detached garages, recessed garages if attached to the principal building and fencing alternatives.  
Garage fronts should be de-emphasized and not be the most prominent architectural feature of the house.  This should be accomplished by providing side access garages, recessed or detached “in-line” garages.  Garages should be recessed at least one car length in order to provide interest and relief from the street.  The front elevation shall prominently feature an entrance for persons rather than automobiles with the garage area not to exceed forty percent (40%) of the front façades.  

Recreation elements

Not less than 70 percent (70%) of the residential units are located within 660 feet of a pedestrian, equestrian and bicycle trail or park of at least 4,000 sf or more.  
Recreation and open space facilities should be aligned with the community’s park and open space network. 
Neighborhood scale recreation facilities and amenities should be provided which are functional, not retention/detention or amenities should be designed in accordance with the Stormwater Management Standards of this Ordinance.  Such areas should include turf or landscaping within all areas not permanently covered with standing water.  
Gateway treatments may be incorporated at appropriate locations along an open space network.  

Transportation elements 

Park-and-ride lots and bus stations may be incorporated with planned facilities.  
Bicycle lanes should be included along at least seventy percent (70%) of the linear frontage of all planned collector streets. 



Landscaping and Buffering elements 

Higher density or intensity developments abutting lower density or intensity areas include buffering and should substantially mitigate any negative impacts consistent with the Landscaping Standards of this Ordinance.  

 Other Design considerations

Homeowners or property owners associations should be required to maintain all roadway/right-of-way landscaping, pedestrian/bicycle, and equestrian paths (arterial, collector and local as proposed) to the standards of this Ordinance.  
Areas designated for industrial land uses should be designed to create a campus-style environment.  
Street access must be provided to all undeveloped and under-developed parcels.  

Section 3.18 ◊a[#:name "Flood Damage Prevention Overlay District"]{Flood Damage Prevention Overlay District}

3.18.1 Statutory Authorization 

The Legislature of the State of North Carolina has in Part 6, Article 21 of Chapter 143; Parts 3, 5, and 8 of Article 19 of Chapter 160A; and Article 8 of Chapter 160A of the North Carolina General Statutes, delegated to local governmental units the responsibility to adopt regulations designed to promote the public health, safety, and general welfare.

Therefore, the Town Council of the Town of China Grove, North Carolina, does ordain as follows:

3.18.2 Findings of Fact 

The flood prone areas within the jurisdiction of the Town of China Grove are subject to periodic inundation which results in loss of life, property, health and safety hazards, disruption of commerce and governmental services, extraordinary public expenditures of flood protection and relief, and impairment of the tax base, all of which adversely affect the public health, safety, and general welfare.

These flood losses are caused by the cumulative effect of obstructions in floodplains causing increases in flood heights and velocities and by the occupancy in flood prone areas of uses vulnerable to floods or other hazards.

3.18.3 Statement of Purpose 

It is the purpose of this section is to promote public health, safety, and general welfare and to minimize public and private losses due to flood conditions within flood prone areas by provisions designed to:

Restrict or prohibit uses that are dangerous to health, safety, and property due to water or erosion hazards or that result in damaging increases in erosion, flood heights or velocities;

Require that uses vulnerable to floods, including facilities that serve such uses, be protected against flood damage at the time of initial construction;

Control the alteration of natural floodplains, stream channels, and natural protective barriers, which are involved in the accommodation of floodwaters;

Control filling, grading, dredging, and all other development that may increase erosion or flood damage; and
Prevent or regulate the construction of flood barriers that will unnaturally divert flood waters or which may increase flood hazards to other lands.

3.18.4 Objectives 

The objectives of this section are to:

Protect human life, safety, and health;

Minimize expenditure of public money for costly flood control projects;

Minimize the need for rescue and relief efforts associated with flooding and generally undertaken at the expense of the general public;

Minimize prolonged business losses and interruptions;

Minimize damage to public facilities and utilities (i.e. water and gas mains, electric, telephone, cable and sewer lines, streets, and bridges) that are located in flood prone areas;

Help maintain a stable tax base by providing for the sound use and development of flood prone areas; and

Ensure that potential buyers are aware that property is in a Special Flood Hazard Area.

3.18.5 Lands to Which this Section Applies 

This section shall apply to all Special Flood Hazard Areas within the jurisdiction, including Extra-Territorial Jurisdictions (ETJs), of the Town of China Grove and within the jurisdiction of any other community whose governing body agrees, by resolution, to such applicability.

3.18.6 Basis for Establishing the Special Flood Hazard Areas 

The Special Flood Hazard Areas are those identified under the Cooperating Technical State (CTS) agreement between the State of North Carolina and FEMA in its Flood Insurance Study (FIS) and its accompanying Flood Insurance Rate Maps (FIRM), for Rowan County, dated June 16, 2009, which are adopted by reference and declared to be a part of this ordinance. 

The initial Flood Insurance Rate Maps are as follows for the jurisdictional areas at the initial date:
Rowan County Unincorporated Area, dated November 1, 1979
Town of China Grove Incorporated Area, dated March 29, 1978

3.18.7 Establishment of Floodplain Development Permit 

A Floodplain Development Permit shall be required in conformance with the provisions of this ordinance prior to the commencement of any development activities within Special Flood Hazard Areas determined in accordance with the provisions of 3.16.6 of this ordinance.

3.18.8 Compliance 

No structure or land shall hereafter be located, extended, converted, altered, or developed in any way without full compliance with the terms of this ordinance and other applicable regulations.

3.18.9 Abrogation and Greater Restrictions 

This section is not intended to repeal, abrogate, or impair any existing easements, covenants, or deed restrictions.  However, where this ordinance and another conflict or overlap, whichever imposes the more stringent restrictions shall prevail.

3.18.10 Interpretation 

In the interpretation and application of this ordinance, all provisions shall be:

Considered as minimum requirements;

Liberally construed in favor of the governing body; and

Deemed neither to limit nor repeal any other powers granted under State statutes.

3.18.11 Warning and Disclaimer of Liability 

The degree of flood protection required by this ordinance is considered reasonable for regulatory purposes and is based on scientific and engineering consideration.  Larger floods can and will occur.  Actual flood heights may be increased by man-made or natural causes.  This ordinance does not imply that land outside the Special Flood Hazard Areas or uses permitted within such areas will be free from flooding or flood damages.  This ordinance shall not create liability on the part of the Town of China Grove or by any officer or employee thereof for any flood damages that result from reliance on this ordinance or any administrative decision lawfully made hereunder.

3.18.12 Penalties for Violation 

Violation of the provisions of this ordinance or failure to comply with any of its requirements, including violation of conditions and safeguards established in connection with grants of variance or special exceptions, shall constitute a misdemeanor.  Any person who violates this ordinance or fails to comply with any of its requirements shall, upon conviction thereof, be fined not more than $50.00 or imprisoned for not more than thirty (30) days, or both.  Each day such violation continues shall be considered a separate offense.  Nothing herein contained shall prevent the Town of China Grove from taking such other lawful action as is necessary to prevent or remedy any violation.

