9.4.8 Improvement Installation and Guarantees
Upon approval Engineering Drawings, the developer may proceed with the installation of or arrangement for required improvements in accordance with the approved Plat and the requirements of this Ordinance.  Prior to approval of a final plat, the developer shall have installed the improvements specified in this Ordinance or guaranteed their installation as provided herein.  

Agreement and Security Required:  In lieu of requiring the completion, installation and dedication of all improvements prior to final plat approval, the Town of China Grove may enter into an agreement with the developer whereby the developer shall agree to complete all required improvements.  Once said agreement is signed by both parties and the security required herein is provided, the final plat may be approved by the China Grove Town Council Commissioners, if all other requirements of this Ordinance are met.  To secure this agreement, the developer shall provide to the Town Council either one, or a combination of the following guarantees shown.  The amount of such guarantee shall be equal to 1.25 times the cost of installing all required improvements, as determined by the Town Engineer.  All such guarantees shall be subject to the approval of the Town Council and shall be made payable to the Town of China Grove.

Surety Performance Bond(s):  The developer shall obtain one or more performance bond(s) from a surety bonding company authorized to do business in North Carolina.  The duration of the bond(s) shall be until such time as the improvements are accepted by the Town Council.

Cash or Equivalent Security:  The developer shall deposit cash, an irrevocable letter of credit, or other instrument readily convertible into cash at face value, either with the Town or in escrow with a financial institution designated as an official depository of the Town of China Grove.

If cash or other instrument is deposited in escrow with a financial institution as herein provided, the developer shall then file with the Town Council an agreement between the financial institution and himself guaranteeing the following:

That said escrow account shall be held in trust until released by the Town Council and may not be used or pledged by the developer in any other matter during the term of the escrow; and

That in case of a failure on the part of the developer to complete said improvements, the financial institution  shall, upon notification by the Town Council and submission of the Town Engineer's estimate of the amount needed to complete the improvements by the Town Council to the financial institution immediately either pay to the Town the funds estimated to complete the improvements, up to the full balance of the escrow account, or deliver to the Town any other instruments fully endorsed or otherwise made payable in full to the Town.				
Default:  Upon default, meaning failure on the part of the developer to complete the required improvements in a timely manner as spelled out in the performance bond or escrow agreement, then the surety, or the financial institution holding the escrow account, shall, if requested by the Town Council, pay all or any portion of the bond or escrow fund to the Town of China Grove up to the amount needed to complete the improvements based on the Town Engineer's estimate. Upon payment, the Town Council, in its discretion, may expend such portion of said funds as it deems necessary to complete all or any portion of the required improvements.  The Town shall return to the bonding firm any funds not spent in completing the improvements.  Should the amount of funds needed to complete the installation of all required improvements exceed the amount in the bond or escrow account, the developer shall nonetheless be responsible for providing the funds to cover such costs.  The developer shall at all times bear the financial burden for the installation of all required improvements.

Release of Guarantee Security:  The Town Council may authorize the Subdivision Administrator to release a portion of any security posted as the improvements are completed and approved by the County.  Such funds shall then be released within ten (10) days after the corresponding improvements have been so approved.

9.4.9 Final Plat Approved
If the Plan is found to meet all of the applicable regulations of this Ordinance, then the Zoning Administrator shall approve the final plat.


