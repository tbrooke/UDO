Section 9.1 Purpose and Applicability

The purpose of this Chapter is to establish an orderly process to develop land within the Town of China Grove. It is also the intent of this Chapter to provide a clear and comprehensible development process that is fair and equitable to all interests including the petitioners, affected neighbors, Town staff, related agencies, the Planning Board, and the Town Council.  Approved plans shall be the guiding documents for final approval and permitting.

The development review process applies to all new developments within the Town of China Grove except for existing individual lots for single-family detached residential and two-family residential (duplex) development.  The provisions of this chapter shall be applicable to all Minor and Major Subdivisions, Minor and Major Site Plans, Conditional Use Site Plans, and Vested Rights Site Plans.  The Zoning Administrator or designee may waive the required development review process only in the following cases when he determines that the submission of a development plan in accordance with this Chapter would serve no useful purpose:

Accessory structures

Any enlargement of a principal building by less than 20 percent of its existing size provided such enlargement will not result in parking or landscaping improvements

A change in principal use where such change would not result in a change in lot coverage, parking, or other site characteristics



























