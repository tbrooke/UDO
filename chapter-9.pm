Section 9.4 Minor Subdivision Approval

9.4.1 Minor Subdivision
A minor subdivision is defined as a subdivision where all the following conditions exist: 
No public right-of-way dedication; and 
The entire tract to be subdivided is ten (10) acres or less in size; and 
The resultant subdivision will produce five (5) or fewer lots.  

A preliminary plat shall not be required.  Engineering Drawings may be required prior to the approval of a final plat depending on the circumstances of the Subdivision.  

9.4.2 Preapplication Meeting & Sketch Plan 
