Section 9.7 Subdivision Procedures

9.7.1 School Site Reservation
If the Town Council and the Rowan-Salisbury School System have jointly determined the specific location and size of any school sites to be reserved, Planning Staff shall immediately notify the Board of Education in writing whenever a sketch plan or for a subdivision is submitted which includes all or part of a school site to be reserved.  The Board of Education shall promptly decide whether it still wishes the site to be reserved.  If the Board of Education does wish to reserve the site, the subdivision shall not be approved without such reservation.  The Board of Education shall then have 18 months beginning on the date of final approval of the subdivision within which to acquire the site by purchase or by initiating condemnation proceedings.  If the Board of Education has not purchased or begun proceedings to condemn the site within 18 months, the developer may treat the land as freed of the reservation.

9.7.2 Plat Approval General Procedures
After the effective date of this Ordinance, no subdivision plat of land within the jurisdiction of this Ordinance shall be filed or recorded unless it has first been submitted to the Zoning Administrator and approved by the proper body as set forth in this Ordinance, and until this approval is entered in writing on the face of the plat by the Zoning Administrator.  Such shall not be required of any subdivision of land which, by definition herein, is exempt from the terms of this Ordinance.

The Register of Deeds shall not file or record a plat of a subdivision of land located within the jurisdiction of this Ordinance that has not been first approved in accordance with these provisions.



9.7.3 Statement by Owner
The owner of land shown on a subdivision plat submitted for recording, or his authorized agent, shall sign a statement on the plat stating whether or not any land shown thereon is within the jurisdiction of the Town of China Grove.  

9.7.4 Issuance of Zoning Permits and Conveyance of Subdivision Lots
No zoning permit shall be issued by the Town of China Grove for the erection of any building on any lot within a proposed subdivision until a final plat of said subdivision has been approved in a manner as prescribed by this Ordinance and recorded at the Register of Deeds Office and where applicable, an improvements permit has been issued by the Rowan County Health Department.

After the effective date of this Ordinance, it shall be illegal for any person being the owner or agent of the owner of any land located within the territorial jurisdiction of this Ordinance, to subdivide his land in violation of this Ordinance or to transfer or sell land by reference to, exhibition of, or any other use of a plat showing a subdivision of the land before the plat has been properly approved under the terms of this Ordinance.

The description by metes and bounds in the instrument of transfer or other document used in the process of selling or transferring land shall not exempt the transaction from this penalty.  The Town Council, through its attorney or other official so designated, may enjoin an illegal subdivision, transfer or sale of land by action for injunction.  Further, violators of this Ordinance shall be subject, upon conviction, to fine and/or imprisonment as provided by NCGS 14-4.  Civil penalties may be issued in accordance with Chapter 13.

9.7.5 Subdivision Exceptions
This section shall be applicable to all subdivisions, except those located in a Water Supply Watershed.  The Planning Board may authorize exceptions for Subdivisions from any portion of this Ordinance when, in its opinion, undue hardship may result from their strict compliance.  In granting an exception, the Planning Board shall hold a quasi-judicial public hearing and make the findings required herein, taking into account the nature of the proposed Subdivision, the existing use of land in the vicinity, the number of persons to reside or work in the proposed Subdivision and the probable effect of the proposed Subdivision upon traffic conditions in the vicinity.  No relief shall be granted unless it is found:

That there are special circumstances or conditions affecting said property such that the strict application of the provisions of this Ordinance would deprive the applicant of the reasonable use of his land; and

That the relief is necessary for the preservation and enjoyment of a substantial property right of the petitioner; and

That the circumstances giving rise to the need for the relief are peculiar to the subdivision and are not generally characteristic of other subdivisions in the jurisdiction of this Ordinance; and

That the granting of the relief will not be detrimental to the public health, safety and welfare or injurious to other property in the area in which said property is situated.

Every decision of the Planning Board pertaining to the granting of Subdivision exceptions shall be subject to review by the Superior Court Division of the General Courts of Justice of the State of North Carolina by proceedings in the nature of certiorari.  Any petition for review by the Superior Court shall be duly verified and filed with the Clerk of Superior Court within 30 days after the decision or recommendation of the TRC or Town Council is filed in the Office of the Subdivision Administrator, or after a written copy thereof is delivered to every aggrieved party who has filed a written request for such copy with the Zoning Administrator at the time of the Planning Boardâ€™s hearing of the case, whichever is later.


Section 9.8 Conditional Use Permit Procedures

9.8.1 Purpose and Applicability
This Ordinance provides for a number of uses to be located by right in each general zoning district subject to the use meeting certain area, height, yard and off-street parking and loading requirements.  In addition to these uses, the Ordinance allows some uses on a conditional basis subject to the issuance of a Conditional Use Permit by the Town Council.  The purpose of having such uses being "conditional" is to ensure that they would be compatible with surrounding development and in keeping with the purposes of the general zoning district in which they are located.  Those uses shown as Conditional in the Permitted Uses Table of Section 3.14 shall be subject the requirements and review process of in this Section.

9.8.2 Approval Process
Conditional Use Permits shall follow the Town Council Approval process.  An application shall be filed with the Zoning Administrator and shall be accompanied by a site specific plan meeting the requirements of a Major Site Plan.  A complete application and site plan shall come before the Planning Board for review and recommendation.  The Planning Board may, in its review, suggest reasonable conditions to the location, nature, and extent of the proposed use and its relationship to surrounding properties, parking areas, driveways, pedestrian and vehicular circulation systems, screening and landscaping, timing of development, and any other appropriate conditions.  Such conditions may include dedication of any rights-of-way or easements for streets, water, sewer, or other public utilities necessary to serve the proposed development.  Following Planning Board review and recommendation, the Town Council shall hold a quasi-judicial public hearing and consider the application in accordance with the findings-of-fact in Section 9.10.3 below.

9.8.3 Conditions, Evidence, & Findings-of-Fact
In approving an application for a Conditional Use Permit, the Town Council may attach fair and reasonable conditions to the approval.  The petitioner will have a reasonable opportunity to consider and respond to any additional requirements prior to approval or denial by the Town Council.  In no instance shall any of these conditions be less restrictive than any requirements which would pertain to that particular development found elsewhere in a similar zoning district.  The applicant has the burden of producing competent, material, and substantial evidence to establish the facts and conditions.  If any person submits evidence allegedly contrary to any of the facts or conditions, the burden of proof for overcoming such evidence shall rest with the applicant.
The Town Council shall issue a Conditional Use Permit if it has evaluated an application and determined that:

The use will not materially endanger the public health or safety if located where proposed and developed according to plan, and

The use meets all required conditions and specifications, and

The use will not substantially injure the value of adjoining or abutting property unless the use is a public necessity, and



The location and character of the use, if developed according to the plan as submitted and approved, will be in harmony with the area in which it is to be located and will be in general conformity with the adopted land use plans and other plans for physical development of the China Grove area as adopted by the Town Council.

9.8.4 Effect of Approval & Expiration of Approval
If an application for a Conditional Use Permit is approved by the Town Council, the owner of the property shall have the ability to develop the use in accordance with the stipulations contained in the Conditional Use Permit or develop any other use listed as a "permitted use" for the general zoning district in which it is located.  Any Conditional Use Permit so authorized shall be perpetually binding to the property included in such permit unless subsequently changed or amended by the Town Council.  

Unless the Town Council issues a Conditional Use Permit which either is specifically exempt from any time constraints or has some other specified time period for implementation, the applicant must secure a valid building permit within a one (1) year period from date of issuance of the conditional use permit.  In addition, if the project for which a Conditional Use Permit was issued is not complete and a valid building permit is not in place at the end of said twelve month period, the Zoning Administrator shall notify the applicant of either such finding, and within 60 days of said notification, the Planning Board shall make a recommendation concerning the rescission of the conditional use permit to the Town Council.  The Town Council, after having conducted a public hearing to consider the rescission, may then rescind the Conditional Use Permit, or extend the life of the Conditional Use Permit for a specified period of time.  

9.8.5 Alterations to Site & Amendments to Conditional Use Permit
Minor changes may be made with the approval of the Zoning Administrator on a one-time basis only.  Further changes to the development may only be made by the Town Council by amending the Conditional Use Permit.  Any request to materially change the Conditional Use Permit once it has been issued shall be reviewed in entirety through the Town Council approval process. Minor changes are those that: 
will not alter the basic relationship of the proposed development to adjacent property, and
will not increase the gross floor area of any non-residential use by the smaller of 10 percent or ten 10,000 square feet, and
will not decrease the off-street parking ratio or reduce the yards provided at the periphery of the site by greater than five (5) feet 

9.8.6 Reapplication Following Denial
If a request for Conditional Use Permit is denied by the Town Council, a similar application for the same property or any portion thereof shall not be filed until the expiration of a 12 month period from the date of the most recent denial by the Town Council.  This waiting period shall not be applicable where the application for a Conditional Use Permit is substantially different from the original application.  The term "substantially different" as herein applied shall mean:
The proposed principal use is different than the use contained in the original application; or
The gross floor area of the proposed development is 50 percent or more smaller than contained in the original application.







Section 9.9 Rezoning Procedures

Rezoning (zoning map amendments) shall follow the approval process in Chapter 13.  

9.9.1 Standard Rezonings
For Standard Rezonings, a site specific plan is not required.  Approval of a Standard Rezoning shall result in an official amendment to the Town of China Grove Zoning Map.  Any development of the property shall be reviewed and approved in accordance with the appropriate procedure depending on the type of development proposed.

9.9.2 Conditional Use District Rezonings
For Conditional Use District Rezonings, a site specific plan meeting the requirements for a Major Site Plan or Major Subdivision is required.  Approval of a Conditional Use Rezoning shall result in official amendment to the Town of China Grove Zoning map. 


Section 9.10 Vested Rights Procedures

Pursuant to NCGS 160A-385.1 and not withstanding any other provision of this Ordinance or amendment thereto, a landowner may apply for a site specific development plan approval which shall entitle said landowner to develop property in accordance with said site specific plan.  To apply for vested right, a landowner shall first submit to the Zoning Administrator a site specific plan.  The plan shall be submitted in accordance with the requirements for a Major Site Plan or Major Subdivision.  

9.10.1 Approval Criteria
In approving an application for vested rights of a site specific plan, the Town Council may attach fair and reasonable ad hoc conditions which tend to support the requiring finding of facts as herein listed.  The petitioner shall be given reasonable opportunity to consider and respond to any additional requirements prior to approval or denial by the Town Council.  The Town Council may not require the landowner to waive his vested right as a condition of developmental approval.

The Town Council may approve the site specific plan if it has evaluated an application and determined that:

The use meets all required specifications of the Zoning Ordinance, and

The use will not materially endanger the public health or safety and will not substantially injure the value of adjoining property if located where proposed.  Conditions, if any, placed on the site specific development plan by the Town Council shall be adequate to meet this requirement.

If the site specific development plan is vested for a period of greater than two (2) years, this shall be based on one or more factors so described in Section 9.12.2 (A).

The burden of proof of producing evidence to support these findings (and to overcome any challenges that approval of the site plan would be contrary to one or more of these findings shall rest entirely with the landowner.




If the use or development for which the site specific development plan is submitted is a conditional use, the Town Council may approve the site specific development plan contemporaneously with the approval of the Conditional Use Permit.  In no case, however, may a site specific development plan be approved for a use or development which requires the issuance of a conditional use permit without the conditional use permit having first been issued.

9.10.2 Effect of Approval
The effect of the Town Council approving a site-specific plan shall be to vest such site plan for a period of two (2) years from the date of approval.  If the landowner requests, however, the Town Council may approve a vesting period not to exceed five (5) years from the date of approval.  The vesting of any site plan beyond a two (2) year period may only be authorized by the Town Council where it is found that due to:
sizing and phasing of the development; or 
level of investment; or 
need for the development; or 
economic cycles; or 
market conditions, building permits for all phases of the development cannot be secured within two (2) years

A vested right shall confer upon the landowner the right to undertake and complete the development and use of said property under the terms and conditions of the site specific development plan as provided for in this Section.  Failure to abide by the terms and conditions placed upon such approval will result in the forfeiture of the vested right previously accorded.

A vested right, once established as herein provided, shall preclude any zoning action by the Town which would change, alter, impair, prevent, diminish or otherwise delay the development or use of the property as set forth in the approved site specific development except under the following conditions:

The affected landowner provides written consent to the Town of his desire to terminate the vested right; or

The Town determines, after having advertised and held a public hearing, that natural or man-made hazards exist on or in the immediate vicinity of the property which pose a serious threat to the public health, safety and welfare if the project were to proceed as indicated in the site specific development plan; or

Compensation is made by the Town to the landowner for all costs, expenses, and other losses incurred including, but not limited to, all fees paid in consideration of financing, and all architectural, planning, marketing, legal, and any other consultant's fees incurred after approval together with interest thereon at the legal rate until paid; or

The Town determines, after having advertised and held a public hearing, that the landowner or his representative intentionally supplied inaccurate information or made material misrepresentations which made a difference in the approval by the Town of the site specific development plan; or

Upon the enactment or promulgation of a State or Federal law or regulations which precludes development as contemplated in the site-specific development plan.  In such case the Town may (after having advertised and conducted a public hearing) modify the affected provisions upon a finding that the change in State or Federal law has a fundamental effect on the plan.

Once a vested right is granted to a particular site specific plan, nothing in this section shall preclude the Town from conducting subsequent reviews and approvals to ensure compliance with the terms and conditions of the original approval, provided such reviews and approvals are not inconsistent with the original approval.

9.10.3 Revocation or Expiration of a Vested Right
The vested right resulting from the approval of a site specific plan may be revoked by the Town Council.  In addition, a revocation may occur if the Town Council determines that the landowner has failed to comply with the terms and conditions of the approval or with any other applicable portion of the Zoning Ordinance.  The vested right shall otherwise expire at the end of the approval period established by the Town Council.

A building permit issued by the Rowan County Building Inspector pursuant to NCGS 160A-417 may not be revoked because of the running of time on a piece of property for which a site-specific development plan has been approved and the vested right period has not otherwise expired.

The establishment of a vested right on a piece of property for a site specific plan shall not preclude the Town from establishing and enforcing on the property any additional regulations (adopted during the time the vested right was in effect) which are general in nature and applicable to all property subject to the regulations of this Ordinance.















